public class DeretAngkaA {
    public static void main(String[] args) {
        int angka = 1;
        System.out.print(angka + " ");
        while (angka < 10){
            angka ++;
            System.out.print(angka + " ");
            angka += 2;
            System.out.print(angka + " ");
            angka --;
            System.out.print(angka + " ");
        }
    }
}